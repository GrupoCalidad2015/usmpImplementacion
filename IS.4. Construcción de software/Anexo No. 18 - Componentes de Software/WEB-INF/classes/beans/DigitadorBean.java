package beans;

public class DigitadorBean {

	int idt_Digitador;
	
	String nomDig;
	String apeDig;
	String dni;
	String usuario;
	String contraseña;
	
	public int getIdt_Digitador() {
		return idt_Digitador;
	}
	public void setIdt_Digitador(int idt_Digitador) {
		this.idt_Digitador = idt_Digitador;
	}
	public String getNomDig() {
		return nomDig;
	}
	public void setNomDig(String nomDig) {
		this.nomDig = nomDig;
	}
	public String getApeDig() {
		return apeDig;
	}
	public void setApeDig(String apeDig) {
		this.apeDig = apeDig;
	}
	public String getDni() {
		return dni;
	}
	public void setDni(String dni) {
		this.dni = dni;
	}
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public String getContraseña() {
		return contraseña;
	}
	public void setContraseña(String contraseña) {
		this.contraseña = contraseña;
	}
	
	
}
